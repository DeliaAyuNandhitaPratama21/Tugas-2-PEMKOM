     private void loadData() {
        DefaultTableModel model = (DefaultTableModel) tabelBarang.getModel();
        model.setRowCount(0);
        List<? extends Item> list = Inventory.getData();
        for (Item inv : list) {
            model.addRow(new Object[]{inv.getKodeBarang(), inv.getNamaBarang(), inv.getKategoriBarang(), inv.getMerek(), inv.getJumlah()});
        }
    }

