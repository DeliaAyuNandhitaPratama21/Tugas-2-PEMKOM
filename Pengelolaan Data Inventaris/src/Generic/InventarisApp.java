    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        int row = tabelBarang.getSelectedRow();
        if (row != -1) {
            String kodeBarang = tabelBarang.getValueAt(row, 0).toString();
            Inventory.hapusData(kodeBarang);
            loadData();
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        Item inv = new Item(
                txtKodeBarang.getText(), txtNamaBarang.getText(), cmbKategoriBarang.getSelectedItem().toString(),
                txtMerek.getText(), Integer.parseInt(txtJumlah.getText()));
        Inventory.tambahData(inv);
        loadData();
        
        txtKodeBarang.setText("");
        txtNamaBarang.setText("");
        cmbKategoriBarang.setSelectedIndex(0);
        txtMerek.setText("");
        txtJumlah.setText("");
    }//GEN-LAST:event_btnTambahActionPerformed

